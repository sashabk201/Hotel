package wraps;

public class EmployeeType {
	private String id;
	private String WorkTypeName;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWorkTypeName() {
		return WorkTypeName;
	}
	public void setWorkTypeName(String workTypeName) {
		WorkTypeName = workTypeName;
	}
}
