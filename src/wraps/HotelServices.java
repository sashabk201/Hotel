package wraps;

public class HotelServices {

	private String IDHotelServices;
	private String ServicesName;
	private String ServicesPrice;
	public String getIDHotelServices() {
		return IDHotelServices;
	}
	public void setIDHotelServices(String iDHotelServices) {
		IDHotelServices = iDHotelServices;
	}
	public String getServicesName() {
		return ServicesName;
	}
	public void setServicesName(String servicesName) {
		ServicesName = servicesName;
	}
	public String getServicesPrice() {
		return ServicesPrice;
	}
	public void setServicesPrice(String servicesPrice) {
		ServicesPrice = servicesPrice;
	}

}
