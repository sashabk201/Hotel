package wraps;

public class Employe {
	
	private String id;
	private String EmployeeName;
	private String EmployePhone;
	private String EmployeePasport;
	private String EmployesAdress;
	public String getEmployeeName() {
		return EmployeeName;
	}
	public void setEmployeeName(String employeeName) {
		EmployeeName = employeeName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEmployeePasport() {
		return EmployeePasport;
	}
	public void setEmployeePasport(String employeePasport) {
		EmployeePasport = employeePasport;
	}
	public String getEmployePhone() {
		return EmployePhone;
	}
	public void setEmployePhone(String employePhone) {
		EmployePhone = employePhone;
	}
	public String getEmployesAdress() {
		return EmployesAdress;
	}
	public void setEmployesAdress(String employesAdress) {
		EmployesAdress = employesAdress;
	}
	

}
